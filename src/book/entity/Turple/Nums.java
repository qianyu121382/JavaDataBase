package book.entity.Turple;

public class Nums
{
    private int allNum=0;
    private int aliveNum=0;
    private int borrowNum=0;
    private int cirNum=0;

    public int getAllNum()
    {
        return allNum;
    }

    public void setAllNum(int allNum)
    {
        this.allNum = allNum;
    }

    public int getAliveNum()
    {
        return aliveNum;
    }

    public void setAliveNum(int aliveNum)
    {
        this.aliveNum = aliveNum;
    }

    public int getBorrowNum()
    {
        return borrowNum;
    }

    public void setBorrowNum(int borrowNum)
    {
        this.borrowNum = borrowNum;
    }

    public int getCirNum()
    {
        return cirNum;
    }

    public void setCirNum(int cirNum)
    {
        this.cirNum = cirNum;
    }
}
